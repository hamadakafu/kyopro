const ten97: usize = 1000000007;
fn alphabet2idx(c: char) -> usize {
    if c.is_ascii_lowercase() {
        c as u8 as usize - 'a' as u8 as usize
    } else if c.is_ascii_uppercase() {
        c as u8 as usize - 'A' as u8 as usize
    } else {
        panic!("wtf")
    }
}

fn main() {
    let (a, b, c): (usize, usize, usize) = parse_line().unwrap();
    if a < c.pow(b as u32) {
        println!("Yes");
    } else {
        println!("No");
    }
}
